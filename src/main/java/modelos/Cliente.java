package modelos;

/**
 *
 * @author root
 */
public class Cliente {
    private int id;
    private String nombre;
    private int dni;
    private String email;

    public Cliente(int id, String nombre, int dni, String email) {
        this.id = id;
        this.nombre = nombre;
        this.dni = dni;
        this.email = email;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getDni() {
        return dni;
    }

    public void setDni(int dni) {
        this.dni = dni;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return "Cliente{nombre=" + nombre + ", dni=" + dni + ", email=" + email + '}';
    }
}
